<?php

use Tests\Fixtures\TestingNumber;

test('Number can remove superfluous leading zeros', function (string|int $input, string $expect) {
    $result = TestingNumber::removeSuperfluousLeadingZeros($input);

    expect($result)->toBe($expect);
})->with([
    /** Integers */
    [ -PHP_INT_MAX, '-9223372036854775807' ],
    [ -345348753, '-345348753' ],
    [ -1, '-1' ],
    [ 0, '0' ],
    [ 1, '1' ],
    [ 345348753, '345348753' ],
    [ PHP_INT_MAX, '9223372036854775807' ],

    /** String Integers */
    [ '-9223372036854775807', '-9223372036854775807' ],
    [ '-345348753', '-345348753' ],
    [ '-1', '-1' ],
    [ '0', '0' ],
    [ '1', '1' ],
    [ '345348753', '345348753' ],
    [ '9223372036854775807', '9223372036854775807' ],

    /** String Floats */
    [ '-9223372036854.775807', '-9223372036854.775807' ],
    [ '-3453487.53', '-3453487.53' ],
    [ '-1.0', '-1.0' ],
    [ '0.0', '0.0' ],
    [ '1.0', '1.0' ],
    [ '345348.753', '345348.753' ],
    [ '922.3372036854775807', '922.3372036854775807' ],

    /** Leading zeros */
    [ '-0000001.0', '-1.0' ],
    [ '0000000.0', '0.0' ],
    [ '0000001.0', '1.0' ],
    [ '-0000001', '-1' ],
    [ '0000000', '0' ],
    [ '0000001', '1' ],
]);
